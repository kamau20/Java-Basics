public class ArithmeticOperator {
    public static void main(String[] args) {
        int a = 15, b =4;

        System.out.println("a + b = " + (a + b)); // 19
        System.out.println("a - b = " + (a - b)); // 11
        System.out.println("a * b = " + (a * b)); // 60
        System.out.println("a / b = " + (a / b)); // 3 (integer division)
    }
}
