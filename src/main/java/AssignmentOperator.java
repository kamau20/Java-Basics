public class AssignmentOperator {
    public static void main(String[] args) {
        int x = 10; // Basic assignment

        x += 5; // x = x + 5 ¬ 15
        x -= 3; // x = x - 3 ¬ 12
        x *= 2; // x = x * 2 ¬  24
        x /= 4; // x = x / 4 ¬ 6
        x %= 4; // x = x % 4 ¬ 2

        System.out.println("Final x: " + x);
    }
}
